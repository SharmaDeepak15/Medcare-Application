public class HospitalDetails {

    public int getHospital_id() {
        return hospital_id;
    }

    public void setHospital_id(int hospital_id) {
        this.hospital_id = hospital_id;
    }

    public String getHospital_name() {
        return hospital_name;
    }

    public void setHospital_name(String hospital_name) {
        this.hospital_name = hospital_name;
    }

    public long getHospital_contact() {
        return hospital_contact;
    }

    public void setHospital_contact(long hospital_contact) {
        this.hospital_contact = hospital_contact;
    }

    public String getHospital_email() {
        return hospital_email;
    }

    public void setHospital_email(String hospital_email) {
        this.hospital_email = hospital_email;
    }

    public String getHospital_street_name() {
        return hospital_street_name;
    }

    public void setHospital_street_name(String hospital_street_name) {
        this.hospital_street_name = hospital_street_name;
    }

    public String getHospital_city() {
        return hospital_city;
    }

    public void setHospital_city(String hospital_city) {
        this.hospital_city = hospital_city;
    }

    public String getHospital_country() {
        return hospital_country;
    }

    public void setHospital_country(String hospital_country) {
        this.hospital_country = hospital_country;
    }

    public String getHospital_postal_code() {
        return hospital_postal_code;
    }

    public void setHospital_postal_code(String hospital_postal_code) {
        this.hospital_postal_code = hospital_postal_code;
    }
    
     @Override
    public String toString(){
        return hospital_name;
    }
    
    private int hospital_id;
    private String hospital_name;
    private long hospital_contact;
    private String hospital_email;
    private String hospital_street_name;
    private String hospital_city;
    private String hospital_country;
    private String hospital_postal_code;

}
