    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String validate_hospital_id = String.valueOf(HospitalIDTxt.getText());
         if (validate_hospital_id.length() == 0 ){
            JOptionPane.showMessageDialog(this,"The 'ID' is a mandatory field, please provide required value.", "Empty Field", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (validate_hospital_id.length() > 0 ){
            try 
                { 
                Integer.parseInt(HospitalIDTxt.getText()); 
                }  
            catch (NumberFormatException e)  
                { 
                JOptionPane.showMessageDialog(this,"The ID is invalid, please provide the integer value only.", "Invalid Value", JOptionPane.WARNING_MESSAGE);
                return;}          
        } 
        
        
        
        String name = HospitalNameTxt.getText();
        if (name.length() == 0 ){
            JOptionPane.showMessageDialog(this,"The 'Name' is a mandatory field, please provide required value.", "Empty Field", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String validate_hosp_contact = String.valueOf(HospitalContactTxt.getText());
        if (validate_hosp_contact.length() > 0 ){
            try 
                { 
                Integer.parseInt(HospitalContactTxt.getText()); 
                }  
            catch (NumberFormatException e)  
                { 
                JOptionPane.showMessageDialog(this,"The Contact is invalid, please provide the integer value only.", "Invalid Value", JOptionPane.WARNING_MESSAGE);
                return;}          
        } 
        
        String city = HospitalCityTxt.getText();
        if (city.length() == 0 ){
            JOptionPane.showMessageDialog(this,"The 'City' is a mandatory field, please provide required value.", "Empty Field", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        
        
        int hospital_id = Integer.parseInt(HospitalIDTxt.getText());
        String hospital_name = HospitalNameTxt.getText();
        long hospital_contact = Long.parseLong(HospitalContactTxt.getText());
        String hospital_email = HospitalEmailTxt.getText();
        String hospital_streetname = HospitalStreetNameTxt.getText();
        String hospital_city = HospitalCityTxt.getText();
        String hospital_country = HospitalCountryTxt.getText();
        String hospital_postalcode = HospitalPostalCodeTxt.getText();
        
        for (HospitalDetails ed : hospital_history.getHistory()){
        int history_hospital_id = ed.getHospital_id();
        if (hospital_id == history_hospital_id){     
            JOptionPane.showMessageDialog(this,"The 'ID' is already exist, please provide the another value.", "Invalid Value", JOptionPane.WARNING_MESSAGE);
            return;
            }
        }
        
        HospitalDetails HospRec = hospital_history.addNewHospital();       
        HospRec.setHospital_name(hospital_name);
        HospRec.setHospital_id(hospital_id);
        HospRec.setHospital_contact(hospital_contact);
        HospRec.setHospital_email(hospital_email);
        HospRec.setHospital_city(hospital_city);
        HospRec.setHospital_country(hospital_country);
        HospRec.setHospital_postal_code(hospital_postalcode);
        HospRec.setHospital_street_name(hospital_streetname);
        
        JOptionPane.showMessageDialog(this, "The Hospital Details has been saved");

        HospitalIDTxt.setText("");
        HospitalNameTxt.setText("");
        HospitalContactTxt.setText("");
        HospitalEmailTxt.setText("");
        HospitalStreetNameTxt.setText("");
        HospitalCityTxt.setText("");
        HospitalCountryTxt.setText("");
        HospitalPostalCodeTxt.setText("");
        
    }//GEN-LAST:event_jButton1ActionPerformed

